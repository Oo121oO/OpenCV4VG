package com.yy.opencv;

public class HoughCircles {
    final float x;
    final float y;
    final float r;
    /**
     * 构造函数
     *
     * @param x
     * @param y
     * @param r
     */
    public HoughCircles(float x, float y, float r) {
        this.x = x;
        this.y = y;
        this.r = r;
    }
}
